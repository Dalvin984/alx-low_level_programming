#include <stdio.h>

/*
 * Main: Printing all single digits of base 10
 * 	starting from 0,followed by new line.
 *
 * Return: Always 0
 */

int main(void)
{
	for (int i = 0; i < 10; i++)
		
		printf("%d", i);
	
	printf('\n');

	return (0);

}
